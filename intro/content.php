<h1>本館簡介</h1>
<p>本館早期座落於校舍三樓，面積約210平方公尺，於1982年9月正式啟用，初期藏書量只得三千餘本，發展至今藏書量已增至二萬冊。早期圖書館內的自修室更是多用途的特別室，它既是語言實驗室，亦是老師們的會議室，更是浮動班的課室呢！至於圖書館的電腦化，幸得邱藹源校長的大力支持，以及資訊科技組和校務處同事的鼎力幫助，得以在2001年9月正式全面使用電腦化運作，令圖書館進入一新里程。</p>
<p>又於2005年8月初搬至新翼二樓，面積約3000平方尺，地方寛蔽、窗明几靜，窗外綠樹成蔭，誠為一理想的讀書温習好地方。 且於館內新増一自學資源閣，擺放各科老師所提供的光碟，讓同學可在館內自行學習，以養成同學自學的習慣及達致終生學習的目標。</p>
